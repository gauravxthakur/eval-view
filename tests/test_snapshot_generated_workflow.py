"""Tests for approving generated tests before snapshotting."""

from __future__ import annotations

from datetime import datetime

from click.testing import CliRunner

from evalview.core.types import (
    ContainsChecks,
    CostEvaluation,
    EvaluationResult,
    Evaluations,
    ExecutionMetrics,
    ExecutionTrace,
    LatencyEvaluation,
    OutputEvaluation,
    SequenceEvaluation,
    ToolEvaluation,
)


def _passing_result(test_case_name: str) -> EvaluationResult:
    now = datetime.now()
    return EvaluationResult(
        test_case=test_case_name,
        passed=True,
        score=90.0,
        evaluations=Evaluations(
            tool_accuracy=ToolEvaluation(accuracy=1.0),
            sequence_correctness=SequenceEvaluation(
                correct=True,
                expected_sequence=[],
                actual_sequence=[],
            ),
            output_quality=OutputEvaluation(
                score=90.0,
                rationale="ok",
                contains_checks=ContainsChecks(),
                not_contains_checks=ContainsChecks(),
            ),
            cost=CostEvaluation(total_cost=0.0, threshold=1.0, passed=True),
            latency=LatencyEvaluation(total_latency=10.0, threshold=1000.0, passed=True),
        ),
        trace=ExecutionTrace(
            session_id="s1",
            start_time=now,
            end_time=now,
            steps=[],
            final_output="ok",
            metrics=ExecutionMetrics(total_cost=0.0, total_latency=10.0),
        ),
        timestamp=now,
    )


def test_snapshot_blocks_unapproved_generated_tests(monkeypatch, tmp_path):
    """Snapshot should refuse generated draft tests unless approval is explicit."""
    from evalview.commands.snapshot_cmd import snapshot

    monkeypatch.chdir(tmp_path)
    tests_dir = tmp_path / "tests"
    tests_dir.mkdir()
    (tests_dir / "generated.yaml").write_text(
        """# Auto-generated by: evalview generate
name: Generated Case
description: Draft generated by evalview generate.
meta:
  generated_by: evalview generate
  review_status: draft
input:
  query: hi
expected:
  output:
    contains:
      - ok
thresholds:
  min_score: 0
""",
        encoding="utf-8",
    )

    runner = CliRunner()
    result = runner.invoke(snapshot, ["tests"])

    assert result.exit_code == 0
    assert "require approval" in result.output.lower()
    assert "--approve-generated" in result.output


def test_snapshot_approves_generated_tests_before_snapshot(monkeypatch, tmp_path):
    """Approval flag should update YAML metadata and allow snapshot execution."""
    from evalview.commands.snapshot_cmd import snapshot

    monkeypatch.chdir(tmp_path)
    tests_dir = tmp_path / "tests"
    tests_dir.mkdir()
    generated_file = tests_dir / "generated.yaml"
    generated_file.write_text(
        """# Auto-generated by: evalview generate
name: Generated Case
description: Draft generated by evalview generate.
meta:
  generated_by: evalview generate
  review_status: draft
input:
  query: hi
expected:
  output:
    contains:
      - ok
thresholds:
  min_score: 0
""",
        encoding="utf-8",
    )

    monkeypatch.setattr(
        "evalview.commands.snapshot_cmd._load_config_if_exists",
        lambda: None,
    )
    monkeypatch.setattr(
        "evalview.commands.snapshot_cmd._execute_snapshot_tests",
        lambda test_cases, config: [_passing_result(test_cases[0].name)],
    )
    monkeypatch.setattr(
        "evalview.commands.snapshot_cmd._cloud_push",
        lambda saved_names: None,
    )
    monkeypatch.setattr(
        "evalview.core.golden.GoldenStore.save_golden",
        lambda self, result, notes=None, variant_name=None: None,
    )
    monkeypatch.setattr(
        "evalview.core.project_state.ProjectStateStore.is_first_snapshot",
        lambda self: False,
    )
    monkeypatch.setattr(
        "evalview.core.project_state.ProjectStateStore.update_snapshot",
        lambda self, test_count=1: None,
    )

    runner = CliRunner()
    result = runner.invoke(snapshot, ["tests", "--approve-generated"])

    assert result.exit_code == 0, result.output
    assert "Approved 1 generated test" in result.output
    updated = generated_file.read_text(encoding="utf-8")
    assert "review_status: approved" in updated
    assert "approved_at:" in updated
